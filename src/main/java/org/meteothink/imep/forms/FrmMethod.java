    private void jComboBox_MethodTypeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox_MethodTypeActionPerformed
        // TODO add your handling code here:
        if (this.jComboBox_MethodType.getItemCount() > 0){
            MethodType methodType = (MethodType)this.jComboBox_MethodType.getSelectedItem();
            switch (methodType){
                case DICHOTOMOUS:
                    this.jPanel_Dichotomous.setVisible(true);
                    this.jPanel_MultiCategory.setVisible(false);
                    if (this._method.getMethodType() == MethodType.DICHOTOMOUS){      
                        DichotomousMethod dMethod = (DichotomousMethod)this._method;
                        this.jTextField_MinThreshold.setText(String.valueOf(dMethod.getDataRange().getMinThreshold()));
                        this.jTextField_MaxThreshold.setText(String.valueOf(dMethod.getDataRange().getMaxThreshold()));
                    } else {
                        this.jTextField_MinThreshold.setText("null");
                        this.jTextField_MaxThreshold.setText("null");
                    }
                    break;
                case MULTICATEGORY:
                    this.jPanel_Dichotomous.setVisible(false);
                    this.jPanel_MultiCategory.setVisible(true);
                    if (this._method.getMethodType() == MethodType.MULTICATEGORY){
                        MultiCategoryMethod mMethod = (MultiCategoryMethod)this._method;
                        this.jTextField_Values.setText(mMethod.valuesToString());
                    } else {
                        this.jTextField_Values.setText("0.0;1.0");
                    }
                    break;
                case CONTINUOUS:
                    this.jPanel_Dichotomous.setVisible(false);
                    this.jPanel_MultiCategory.setVisible(false);
                    break;
            }
        }
    }//GEN-LAST:event_jComboBox_MethodTypeActionPerformed

    public void setVerifyGroup(VerifyGroup group) {
        _group = group;
        _method = group.getVerifyMethod();
        this.jComboBox_MethodType.removeAllItems();
        for (MethodType type : MethodType.values()) {
            this.jComboBox_MethodType.addItem(type);
        }
        this.jComboBox_MethodType.setSelectedItem(this._method.getMethodType());
    }
    
