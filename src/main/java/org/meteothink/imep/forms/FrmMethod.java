    private void jButton_OKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_OKActionPerformed
        // TODO add your handling code here:
        MethodType methodType = (MethodType) this.jComboBox_MethodType.getSelectedItem();
        VerifyMethod method = new DichotomousMethod();
        switch (methodType) {
            case DICHOTOMOUS:
                DichotomousMethod dMethod = new DichotomousMethod();
                String minTstr = this.jTextField_MinThreshold.getText();
                String maxTstr = this.jTextField_MaxThreshold.getText();
                if (!minTstr.equalsIgnoreCase("null"))
                    dMethod.getDataRange().setMinThreshold(Double.parseDouble(minTstr));
                if (!maxTstr.equalsIgnoreCase("null"))
                    dMethod.getDataRange().setMaxThreshold(Double.parseDouble(maxTstr));
                method = dMethod;
            break;
            case MULTICATEGORY:
                MultiCategoryMethod mMethod = new MultiCategoryMethod();
                mMethod.setValues(this.jTextField_Values.getText());
                method = mMethod;
            break;
            case CONTINUOUS:
                method = new ContinuousMethod();
                break;
        }

        this._group.setVerifyMethod(method);
        this.dispose();
    }//GEN-LAST:event_jButton_OKActionPerformed

